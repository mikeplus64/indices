genCNats :: Int -> String
genCNats = unlines . map (gen . show) . enumFromTo 0
  where
    gen i = "instance CNat " ++ i ++ " where { {-# INLINE cnat #-}; cnat _ = "
            ++ i ++ "}"

genNats :: Int -> String
genNats = unlines . map (gen . show) . enumFromTo 0
  where
    gen i =
      let name   = "n"++i
          inline = "{-# INLINE " ++ name ++ " #-}"
      in inline ++ "; "
         ++ name ++ " :: proxy " ++ i ++ "; "
         ++ name ++ " = undefined"

